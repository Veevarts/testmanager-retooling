SELECT
    CAST(so.ft_id AS NVARCHAR(36))               AS sandbox_ext_id,
    re.REFERENCE                                 AS Description,
    ft.[TYPE]                                    AS ft_type,
    CAST(ft.TRANSACTIONAMOUNT AS NVARCHAR(20))   AS amount,
    CONVERT(NVARCHAR(10), ft.CALCULATEDDATE, 23) AS close_date
FROM #sandbox_opps so
LEFT JOIN FINANCIALTRANSACTION ft ON ft.ID = so.ft_id
LEFT JOIN REVENUE_EXT re ON re.ID = so.ft_id
ORDER BY ft.CALCULATEDDATE DESC;

